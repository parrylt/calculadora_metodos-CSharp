using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace calculadora_metodos
{
    class Calculadora_POO
    {
        public double Somar(double v1, double v2)
        {
            return v1 + v2;
        }

        public double Multiplicar (double v1, double v2)
        {
            return v1 * v2;
        }

        public double Subtracao (double v1, double v2)
        {
            return v1 - v2;
        }

        public double Divisao (double v1, double v2)
        {
            return v1 / v2;
        }
    }
}
